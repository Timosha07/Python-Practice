# 1. Итерация по списку названий
for fruit in ["яблоко", "банан", "вишня"]:
    print("Фрукт:", fruit)


# 2. Использование range для повторения
for i in range(3):
    print("Строка №", i + 1)


# 3. Посимвольный перебор строки
for char in "Code":
    print("Символ:", char)


# 4. Перебор словаря (ключи и значения)
data = {"a": 1, "b": 2}
for key, val in data.items():
    print(f"Ключ: {key}, Значение: {val}")


# 5. Создание списка квадратов чисел
for n in range(1, 6):
    print(f"{n} в квадрате = {n**2}")